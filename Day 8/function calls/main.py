# Simple Function
def greet():
    print("Hello, Good morning")
    print("Hi, Good afternoon")
    print("Hey, Good evening")
    
    
greet()


# Function that allows input
def greet_with_name(name):
    print(f"Hello {name}, Good morning")
    print(f"Hi {name}, Good afternoon")
    print(f"Hey {name}, Good evening")
    
greet_with_name("John")